        // DOMContentLoaded 이벤트는 html태그만 그리면
        // 바로 실행하므로 애니메이션이 이미지가 들어오기전 
        // 이미 실행됨. 따라서 이미지가 들어온 후 실행하도록 
        // "load"이벤트를 별도로 준다!

