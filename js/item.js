// Get방식으로 넘어온 파라미터 처리하기!!!
var pm = location.href;
// location.href의 두얼굴~~~
// 1. 이퀄왼쪽에 쓰면 오른쪽 url값으로 페이지이동
// 2. 이퀄오른쪽에 쓰면 브라우저 url값을 가져온다!
console.log("url값:" + pm);

/// 파라미터 물음표가 없으면 에러가 발생하므로 걸러준다!
// indexOf(찾는문자열) -> 문자열의 순번을 리턴함!
// 만약 찾는 문자열이 없으면 -1을 리턴함!
console.log("문자열 찾기:" + pm.indexOf("?"));
if (pm.indexOf("?") === -1) { //물음표가 없으면
    alert("메인페이지로 이동합니다!");
    location.href = "index.html";
} ///////// if문 //////////////////////

//// url 문자값 가공처리하기 ///////
// 1. ?(물음표)를 기준으로 잘라서 뒤의 배열값을 사용한다!
// JS내장함수 split(자를기준문자열) 를 사용!
pm = pm.split("?")[1]; //물음표로 자름!(배열번호 0,1 중 1번)
console.log("1차가공:" + pm);
pm = pm.split("=")[1]; //이퀄로 자름!(배열번호 1번이 값이다!)
console.log("2차가공:" + pm);
pm = Number(pm); //숫자형변환!
console.log("3차가공:" + pm);


